/*************************************************************
函数名称: v_fault_occur_fault_output		           				
函数功能: 故障发生时，故障继电器动作，调用此函数前需先获取全局互斥锁g_mut_share_data
          如果系统有硅链控制，则4、5、6继电器用于硅链控制，不能用于故障输出和均浮充指示						
输入参数: u8_index -- 继电器索引，0为无效值，不输出        		   				
输出参数: 无
返回值  ：无														   				
**************************************************************/
void v_fault_occur_fault_output(U8_T u8_index)
{
	RLY_CMD_E e_cmd[6] = { ALARM_RELAY1_ON, ALARM_RELAY2_ON, ALARM_RELAY3_ON, ALARM_RELAY4_ON,
	                       ALARM_RELAY5_ON, ALARM_RELAY6_ON };

	if (g_t_share_data.t_sys_cfg.t_sys_param.e_backstage_protrol == BS_ADJUST)
		return;

	if ((g_t_share_data.t_sys_cfg.t_dc_panel.t_dc.e_diode_chain_ctl != NO_DIODE_CHAIN)
		&& ((u8_index == 4)	|| (u8_index == 5) || (u8_index == 6)))
		return;

	if ((u8_index > 0) && (u8_index <= 6))
	{
		v_relay_relay_operation(e_cmd[u8_index-1]);
		g_t_share_data.t_rt_data.t_u8_dc10_fault_out[u8_index] = 1;
	}
}

