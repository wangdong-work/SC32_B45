/*************************************************************
函数名称: v_fault_save_his_fault		           				
函数功能: 保存历史故障记录到dataflash						
输入参数: t_his_fault -- 指向要保存的历史记录        		   				
输出参数: 无
返回值  ：无														   				
**************************************************************/
static void v_fault_save_his_fault(HIS_FAULT_DATA_T *t_his_fault)
{
	U8_T u8_head[6];
	U16_T u16_cnt, u16_index;
	U32_T u32_addr;

	os_mut_wait(g_mut_fetch_his_fault, 0xFFFF);                 //读写历史故障需要持有互斥量

	s32_flash_dataflash_read(DATAFLASH_HIS_FAULT_ADDR, u8_head, sizeof(u8_head));
	
	u16_cnt = ((u8_head[2]<<8) | u8_head[3]);
	u16_index = ((u8_head[4]<<8) | u8_head[5]);

	if ((u8_head[0] == 0x55) && (u8_head[1] == 0xAA)
		&& (u16_cnt <= HIS_FAULT_MAX_CNT) && (u16_index < HIS_FAULT_MAX_CNT))
	{
		if (u16_cnt < HIS_FAULT_MAX_CNT)
			u16_cnt++;

		u32_addr = (DATAFLASH_HIS_FAULT_ADDR + sizeof(u8_head) + u16_index * sizeof(HIS_FAULT_DATA_T));
		u16_index++;
		u16_index %= HIS_FAULT_MAX_CNT;

		u8_head[2] = (U8_T)(u16_cnt>>8);
		u8_head[3] = (U8_T)u16_cnt;
		u8_head[4] = (U8_T)(u16_index>>8);
		u8_head[5] = (U8_T)u16_index;
	}
	else
	{
		u8_head[0] = 0x55;
		u8_head[1] = 0xAA;
		u8_head[2] = 0;
		u8_head[3] = 1;
		u8_head[4] = 0;
		u8_head[5] = 1;
		u32_addr = DATAFLASH_HIS_FAULT_ADDR + sizeof(u8_head);
	}

	s32_flash_dataflash_write(DATAFLASH_HIS_FAULT_ADDR, u8_head, sizeof(u8_head));
	s32_flash_dataflash_write(u32_addr, (U8_T *)t_his_fault, sizeof(HIS_FAULT_DATA_T));

	m_u8_his_fault_state = FAULT_CHANGE;

	os_mut_release(g_mut_fetch_his_fault);               //释放互斥量
}


