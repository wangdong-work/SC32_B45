				os_mut_wait(g_mut_share_data, 0xFFFF);
				u8_node_idx = g_t_share_data.t_sys_cfg.t_sys_param.u8_rect_fault_output;
				v_fault_occur_fault_output(u8_node_idx);
				os_mut_release(g_mut_share_data);
