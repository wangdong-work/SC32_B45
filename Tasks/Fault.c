/*************************************************************
函数名称: v_fault_save_no_resume_fault		           				
函数功能: 将复位前的当前告警记录添加到末复归告警记录中，清除dataflash中的当前告警记录，此函数在系统上电后的初始化部分调用					
输入参数: 无        		   				
输出参数: 无
返回值  ：无														   				
**************************************************************/
static void v_fault_save_no_resume_fault(void)
{
	U8_T  u8_head[4];
	U16_T u16_curr_cnt, u16_no_resume_cnt, u16_move_cnt, u16_move_index, u16_cnt;
	U32_T u32_src_addr, u32_dest_addr;

	memset(u8_head, 0, sizeof(u8_head));
	s32_flash_dataflash_read(DATAFLASH_CURR_FAULT_ADDR, u8_head, sizeof(u8_head));
	u16_curr_cnt = ((u8_head[2]<<8) | u8_head[3]);

	if ((u8_head[0] != 0x55) || (u8_head[1] != 0xAA)
		|| (u16_curr_cnt > CURR_FAULT_MAX_CNT) || (u16_curr_cnt == 0))
	{
		return;
	}

	memset(u8_head, 0, sizeof(u8_head));
	s32_flash_dataflash_read(DATAFLASH_NO_RESUME_FAULT_ADDR, u8_head, sizeof(u8_head));
	u16_no_resume_cnt = ((u8_head[2]<<8) | u8_head[3]);

	if ((u8_head[0] != 0x55) || (u8_head[1] != 0xAA) || (u16_no_resume_cnt > NO_RESUME_FAULT_MAX_CNT))
	{
		u16_no_resume_cnt = 0;
	}

	if (u16_curr_cnt + u16_no_resume_cnt <= NO_RESUME_FAULT_MAX_CNT)
	{
		u32_dest_addr = DATAFLASH_NO_RESUME_FAULT_ADDR + 4 + u16_no_resume_cnt * sizeof(CURR_FAULT_DATA_T);
		u16_no_resume_cnt += u16_curr_cnt;
	}
	else
	{
		u16_move_index = u16_curr_cnt + u16_no_resume_cnt - NO_RESUME_FAULT_MAX_CNT;
		u16_move_cnt = u16_no_resume_cnt - u16_move_index;
		u32_dest_addr = DATAFLASH_NO_RESUME_FAULT_ADDR + 4;
		u32_src_addr = DATAFLASH_NO_RESUME_FAULT_ADDR + 4 + u16_move_index * sizeof(CURR_FAULT_DATA_T);

		while (u16_move_cnt > 0)
		{
			if (u16_move_cnt > CURR_FAULT_MAX_CNT)
				u16_cnt = CURR_FAULT_MAX_CNT;
			else
				u16_cnt = u16_move_cnt;

			s32_flash_dataflash_read(u32_src_addr, (U8_T *)m_t_curr_fault, u16_cnt*sizeof(CURR_FAULT_DATA_T));
			s32_flash_dataflash_write(u32_dest_addr, (U8_T *)m_t_curr_fault, u16_cnt*sizeof(CURR_FAULT_DATA_T));

			u16_move_cnt -= u16_cnt;
			u32_dest_addr += u16_cnt*sizeof(CURR_FAULT_DATA_T);
			u32_src_addr += u16_cnt*sizeof(CURR_FAULT_DATA_T);
		}

		u16_no_resume_cnt = NO_RESUME_FAULT_MAX_CNT;
	}

	u8_head[0] = 0x55;
	u8_head[1] = 0xAA;
	u8_head[2] = (U8_T)(u16_no_resume_cnt>>8);
	u8_head[3] = (U8_T)u16_no_resume_cnt;
	s32_flash_dataflash_write(DATAFLASH_NO_RESUME_FAULT_ADDR, u8_head, 4);

	s32_flash_dataflash_read(DATAFLASH_CURR_FAULT_ADDR+4, (U8_T *)m_t_curr_fault, u16_curr_cnt*sizeof(CURR_FAULT_DATA_T));
	s32_flash_dataflash_write(u32_dest_addr, (U8_T *)m_t_curr_fault, u16_curr_cnt*sizeof(CURR_FAULT_DATA_T));

	u8_head[0] = 0x55;
	u8_head[1] = 0xAA;
	u8_head[2] = 0;
	u8_head[3] = 0;
	s32_flash_dataflash_write(DATAFLASH_CURR_FAULT_ADDR, u8_head, sizeof(u8_head));   //清空当前告警记录
}


